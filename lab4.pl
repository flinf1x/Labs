% Движок программы

start :-
    nl,
    writeln('Экспертная система диагностики Wi-Fi'),
    writeln('Ответьте на несколько вопросов для поиска проблемы.'),
    dialogue(1).

dialogue(Node) :-
    leaf(Node, Advice), !,
    show_result(Advice),
    ask_restart.

dialogue(Node) :-
    ask(Node, Question, Options, NextNodes), !,
    nl,
    writeln(Question),
    get_choice(Options, ChoiceIndex),
    nth0(ChoiceIndex, NextNodes, NextNode),
    dialogue(NextNode).

dialogue(_) :-
    dialogue(999).


% Ввод ответа

get_choice(Options, ChoiceIndex) :-
    length(Options, Count),
    print_options(Options, 1),
    format('Ваш выбор (1-~w): ', [Count]),
    read_line_to_string(user_input, RawInput),
    normalize_space(string(Input), RawInput),
    (
        catch(number_string(Number, Input), _, fail),
        integer(Number),
        Number >= 1,
        Number =< Count
    ->
        ChoiceIndex is Number - 1
    ;
        writeln('Неверный ввод. Введите номер варианта.'),
        get_choice(Options, ChoiceIndex)
    ).

print_options([], _).
print_options([Option | Rest], Number) :-
    format('~w. ~w~n', [Number, Option]),
    NextNumber is Number + 1,
    print_options(Rest, NextNumber).


% Вывод результата

show_result(Advice) :-
    nl,
    writeln('Результат диагностики:'),
    nl,
    print_lines(Advice),
    nl.

print_lines([]).
print_lines([Line | Rest]) :-
    writeln(Line),
    print_lines(Rest).


% Повторный запуск

ask_restart :-
    write('Начать заново? (да/нет): '),
    read_line_to_string(user_input, RawInput),
    normalize_space(string(Input), RawInput),
    string_lower(Input, Lower),
    (
        member(Lower, ["да", "д", "yes", "y"])
    ->
        start
    ;
        writeln('Работа завершена.')
    ).


% Запуск при открытии файла как программы

:- initialization(start, main).
